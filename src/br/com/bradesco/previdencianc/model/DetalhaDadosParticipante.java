package br.com.bradesco.previdencianc.model;

public class DetalhaDadosParticipante {

    private String pessoaExposta;
    private String relacionamento;

    // Getters e Setters
    public String getPessoaExposta() {
        return pessoaExposta;
    }
    public void setPessoaExposta(String pessoaExposta) {
        this.pessoaExposta = pessoaExposta;
    }
    public String getRelacionamento() {
        return relacionamento;
    }
    public void setRelacionamento(String relacionamento) {
        this.relacionamento = relacionamento;
    }
}